//=======================================================================
void lis()
{
  int a[10]={5,3,4,8,6,7,0,9,2,1};
  int i,j;
  int length[10];

  for(i=0;i<10;i++)
  {
    length[i]=1;
    for(j=0;j<i;j++)
    { 
      if(a[j]<=a[i])
      length[i]=MAX(length[i-1],length[j]+1);
      else
      length[i]=length[i-1];
    }
  }

  for(i=0;i<10;i++)
    printf("%d:%d\n",i+1,length[i]);
}
