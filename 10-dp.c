#include<stdio.h>

#define MIN(x,y)  ((x)<=(y))?(x):(y)
#define MAX(x,y)  ((x)>=(y))?(x):(y)
//=======================================================================
//there are coins which are $1 $3 $5,how to select the minimum conis to make up $N
//=======================================================================
void coin()
{
  int v[3]={1,3,5};
  int coins[21]; //mean to achive value $20 need how many coins
  int i,j;

  coins[0]=0;
  for(i=0;i<21;i++)
  {
    for(j=0;j<3&&v[j]<=i;j++)
    {
      coins[i]=MIN(coins[i-1]+1,coins[i-v[j]]+1);
    } 
  }
  for(i=0;i<21;i++)
  printf("%d:%d\n",i,coins[i]);

}
